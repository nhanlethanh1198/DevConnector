const { check, validationResult } = require("express-validator");

// @route 	GET api/auth
// @desc 	Test route
// @access	Public
router.get("/", auth, async (req, res) => {
  try {
    const user = await User.findById(req.user.id).select("-password");
    res.json(user);
  } catch (error) {
    console.error(error.mesage);
    res.status(500).send("Server Error!");
  }
});

